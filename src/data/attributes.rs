use std::collections::BTreeMap;

/// Requirement type for attribute key/value
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Requirement {
    /// Key or value must be provided, but not both
    Exclusive,
    /// Field must be present
    Must,
    /// Field is allowed but optional
    Allowed,
    /// Field is forbidden
    Denied,
}

/// Describes a single Datastar attribute plugin
pub struct AttributeDef {
    pub name: &'static str,
    pub description: &'static str,
    pub key_req: Requirement,
    pub value_req: Requirement,
    pub doc_url: &'static str,
    /// Pro-only attribute?
    pub pro: bool,
    /// Known modifier keys (e.g. "delay", "case", "debounce")
    pub modifier_keys: &'static [&'static str],
}

